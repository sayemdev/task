class QuantityInputDialog extends StatefulWidget {
  @override
  _QuantityInputDialogState createState() => _QuantityInputDialogState();
}

class _QuantityInputDialogState extends State<QuantityInputDialog> {
  String _selectedQuantityType = 'kg'; // Default value
  TextEditingController _quantityAmountController = TextEditingController();

  @override
  Widget build(BuildContext context) {
    return AlertDialog(
      title: Text('Enter Quantity'),
      content: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          TextField(
            controller: _quantityAmountController,
            keyboardType: TextInputType.number,
            decoration: InputDecoration(
              labelText: 'Quantity Amount',
            ),
          ),
          SizedBox(height: 16),
          DropdownButtonFormField<String>(
            value: _selectedQuantityType,
            items: ['kg', 'gram', 'pieces']
                .map((quantityType) => DropdownMenuItem<String>(
              value: quantityType,
              child: Text(quantityType),
            ))
                .toList(),
            onChanged: (value) {
              setState(() {
                _selectedQuantityType = value!;
              });
            },
            decoration: InputDecoration(
              labelText: 'Quantity Type',
            ),
          ),
        ],
      ),
      actions: [
        TextButton(
          onPressed: () {
            Navigator.of(context).pop();
          },
          child: Text('Cancel'),
        ),
        ElevatedButton(
          onPressed: () {
            final double? quantityAmount =
            double.tryParse(_quantityAmountController.text.trim());

            if (quantityAmount != null && quantityAmount > 0) {
              Navigator.of(context).pop(
                  '${quantityAmount.toStringAsFixed(2)} $_selectedQuantityType');
            }
          },
          child: Text('OK'),
        ),
      ],
    );
  }
}
